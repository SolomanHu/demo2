package cn.domain;

public class TestPaper {
	private int e_id;
	private int e_score;
	private double long_time;
	private String name;
	public int getE_id() {
		return e_id;
	}
	public void setE_id(int eId) {
		e_id = eId;
	}
	public int getE_score() {
		return e_score;
	}
	public void setE_score(int eScore) {
		e_score = eScore;
	}
	public double getLong_time() {
		return long_time;
	}
	public void setLong_time(double longTime) {
		long_time = longTime;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
